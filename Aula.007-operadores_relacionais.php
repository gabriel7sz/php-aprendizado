        Os operadores de comparação ou relacionais são capazes de formar expressões que serão colocadas dentro dos
        comandos if, ou seja, através dos operadores de comparação as condições dos if's irão responder true ou false,
        para seguir um determinado fluxo na lógica da aplicação.

        O php disponibiliza nove operadores de comparação. Com esses operadores é possível responder a todas as perguntas
        para tomada de decisão dentro das aplicações.

        Segue os operadores:
